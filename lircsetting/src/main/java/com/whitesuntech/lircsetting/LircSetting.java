    void broadcastMessage(String message)
    {
        Intent intent = new Intent();
        intent.setAction(PROP_HTTPDOWNLOAD_INTENTID);
        intent.putExtra(HTTPDOWNLOAD_MESSSAGEFIELD, message);
        intent.addFlags(Intent.FLAG_INCLUDE_STOPPED_PACKAGES);
        sendBroadcast(intent);

    }



    void doSubmit()
    {
        if(!CommonlyUsed.stringIsNullOrEmpty(_customname.getText().toString())) {
            String newname = _customname.getText().toString();
            processCSV csv = new processCSV(StorageProperties.getLircFile(),"|","#");
            if(!CommonlyUsed.stringIsNullOrEmpty(csv.getCSVItemForOneValueOfColumn(1,newname,2)))
            {
                CommonlyUsed.showMsg(getApplicationContext(),"Name is already in place");
                return;

            }

            if(CommonlyUsed.stringIsNullOrEmpty(_remotetype.getSelectedText()))
            {
                CommonlyUsed.showMsg(getApplicationContext(),"Select remote type");
                return;

            }



            String existing = fileReadWrite.readStringFromFile(StorageProperties.getLircFile());
            existing += _customname.getText().toString() + "|" + _remotetype.getSelectedText() + "\n";


            broadcastMessage("1234|LIRC_ADD|" + _customname.getText().toString() + "," + _remotetype.getSelectedText() );

            fileReadWrite.writeIntoFile(existing,StorageProperties.getLircFile());



            _tv_view.setText(existing.replace("|"," : "));

            _customname.setText("");
        }
        else
        {
            CommonlyUsed.showMsg(getApplicationContext(),"Please enter a valid name");
        }
    }

